#include <iostream>
using namespace std;

int main() {
    //declare variable
    int myNum = 5;              //Integer
    float myFloatNum = 3.37;    //Floating
    double myDoubleNum = 4.14;  //Floating
    char myLetter = 'S';        //Character
    bool myBoolean = true;      //Boolean 
    string myText = "Sparta";   //String

    //print
    cout << "Various data types in programming!" << "\n";
    cout << "==================================" << "\n";
    cout << "This is an integer: " << myNum << "\n";
    cout << "This is a float: " << myFloatNum << "\n";
    cout << "This is a double: " << myDoubleNum << "\n";
    cout << "This is a char" << myLetter << "\n";
    cout << "This is a boolean" << myBoolean << "\n";
    cout << "This is a string" << myText << "\n";
};