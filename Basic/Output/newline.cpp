#include <iostream>
using namespace std;

int main() {
    cout << "Hello World \n";                           //To make a new line, use ("\n") or (<< "\n")
    cout << "Today is a wonderful day!" << "\n\n";      //The use of "\n\n" will create a blank line
    cout << "and so will be tomorrow!" << endl;         //Alter. way to make a blank line "endl"
    cout << "Goodbye!";
    return 0;
};  